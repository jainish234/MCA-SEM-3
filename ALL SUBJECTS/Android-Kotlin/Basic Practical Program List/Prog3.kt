//3. Write a program that accept two numbers and perform all basic mathematicaloperation and print.

fun main()
{
    val a=100
    val b=60
    val add = a+b
    println("Add: $a + $b = $add")
    val sub = a-b
    println("Sub: $a - $b = $sub")
    val multiply = a*b
    println("Multiply: $a * $b = $multiply")
    val division = a/b
    println("Division: $a / $b = $division")
    val modulus = a%b
    println("Modulus: $a % $b = $modulus")
}

